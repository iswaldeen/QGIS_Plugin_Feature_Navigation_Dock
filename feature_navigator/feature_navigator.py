    def connect_layer_feature_signals(self, layer):
        """Connect to layer signals that affect the feature list."""
        if not layer:
            return

        try:
            layer.featureAdded.connect(self.on_layer_feature_added)
        except TypeError:
            pass

        try:
            layer.featureDeleted.connect(self.on_layer_feature_deleted)
        except TypeError:
            pass


    def disconnect_layer_feature_signals(self, layer):
        """Disconnect from layer signals that affect the feature list."""
        if not layer:
            return

        try:
            layer.featureAdded.disconnect(self.on_layer_feature_added)
        except TypeError:
            pass

        try:
            layer.featureDeleted.disconnect(self.on_layer_feature_deleted)
        except TypeError:
            pass


    def refresh_feature_list(self, preserve_position=True, deleted_fid=None):
        """Rebuild feature id list while preserving current navigation position."""
        layer = self.safe_current_layer()
        if not layer:
            self.current_layer = None
            self.feature_ids = []
            self.current_index = -1
            self.has_selected_feature = False
            self.disable_navigation()
            return

        old_feature_ids = list(self.feature_ids)
        old_index = self.current_index

        current_fid = None
        if preserve_position and old_feature_ids and 0 <= old_index < len(old_feature_ids):
            current_fid = old_feature_ids[old_index]

        self.feature_ids = [f.id() for f in layer.getFeatures()]
        self.feature_ids.sort()
        num_features = len(self.feature_ids)

        if num_features == 0:
            self.current_index = -1
            self.has_selected_feature = False
            self.dockwidget.selectedFeatureLabel.setVisible(False)
            self.dockwidget.selectedFeatureLabel.setText("")
            self.dockwidget.errorLabel.setText("No features in currently selected layer")
            self.dockwidget.errorLabel.setVisible(True)

            if self.timer is not None and self.timer.isActive():
                self.timer.stop()

            self.disable_navigation()
            self.dockwidget.mMapLayerComboBox.setEnabled(True)
            return

        if preserve_position and current_fid in self.feature_ids:
            self.current_index = self.feature_ids.index(current_fid)
        elif deleted_fid is not None and old_feature_ids:
            deleted_index = old_feature_ids.index(deleted_fid) if deleted_fid in old_feature_ids else old_index

            if deleted_index >= len(self.feature_ids):
                self.current_index = len(self.feature_ids) - 1
            else:
                self.current_index = deleted_index
        else:
            if not preserve_position:
                self.current_index = -1
                self.has_selected_feature = False
            elif old_index < 0:
                self.current_index = -1
                self.has_selected_feature = False
            elif old_index >= len(self.feature_ids):
                self.current_index = len(self.feature_ids) - 1
            else:
                self.current_index = old_index

        if num_features == 1:
            self.dockwidget.errorLabel.setText("Only single feature in selected layer")
            self.dockwidget.errorLabel.setVisible(True)
        else:
            self.dockwidget.errorLabel.setVisible(False)

        is_playing = self.timer is not None and self.timer.isActive()

        self.dockwidget.playButton.setEnabled(num_features > 1 and not is_playing)
        self.dockwidget.pauseButton.setEnabled(num_features > 1 and is_playing)
        self.dockwidget.previousButton.setEnabled(num_features > 1 and not is_playing)
        self.dockwidget.nextButton.setEnabled(num_features > 1 and not is_playing)

        if self.current_index >= 0 and self.has_selected_feature:
            self.dockwidget.selectedFeatureLabel.setVisible(True)
            self.update_selected_feature_label()
        else:
            self.current_index = -1
            self.has_selected_feature = False
            self.dockwidget.selectedFeatureLabel.setVisible(False)
            self.dockwidget.selectedFeatureLabel.setText("")


    def on_layer_feature_added(self, fid):
        """Triggered when a feature is added."""
        if self.ignore_selection_change:
            return

        self.refresh_feature_list(preserve_position=True)


    def on_layer_feature_deleted(self, fid):
        """Triggered when a feature is deleted."""
        if self.ignore_selection_change:
            return

        self.refresh_feature_list(preserve_position=True, deleted_fid=fid)

        # Do not auto-select or auto-zoom after deletion.
        # Leave the canvas/view where it is, and wait for the user's next input.
        self.has_selected_feature = False

        if self.dockwidget:
            self.dockwidget.selectedFeatureLabel.setVisible(False)
            self.dockwidget.selectedFeatureLabel.setText("")
        
    def safe_current_layer(self):
        """Return current layer if still valid, otherwise None."""
        layer = getattr(self, "current_layer", None)

        if layer is None:
            self.current_layer = None
            return None

        try:
            _ = layer.id()
            return layer
        except RuntimeError:
            self.current_layer = None
            return None


    def clear_plugin_state(self):
        """Clear plugin state safely when project/layers are removed."""
        if self.timer is not None and self.timer.isActive():
            self.timer.stop()

        old_layer = self.safe_current_layer()
        if old_layer:
            self.disconnect_layer_selection_signal(old_layer)
            self.disconnect_layer_feature_signals(old_layer)
            try:
                old_layer.removeSelection()
            except RuntimeError:
                pass

        self.current_layer = None
        self.feature_ids = []
        self.current_index = -1
        self.has_selected_feature = False

        if self.dockwidget:
            self.dockwidget.selectedFeatureLabel.setVisible(False)
            self.dockwidget.selectedFeatureLabel.setText("")
            self.dockwidget.errorLabel.setVisible(False)
            self.dockwidget.mMapLayerComboBox.setLayer(None)
            self.dockwidget.mMapLayerComboBox.setEnabled(True)
            self.disable_navigation()
        


